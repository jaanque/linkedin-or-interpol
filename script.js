// --- Timer and Game Mode Functions ---

function startTimer() {
    if (gameMode !== 'time-trial') return;
    timerContainer.classList.remove('hidden');
    timeLeft = 10;
    timerSpan.textContent = timeLeft;
    timerInterval = setInterval(() => {
        timeLeft--;
        timerSpan.textContent = timeLeft;
        if (timeLeft <= 3) {
            timerSpan.classList.add('blinking');
        } else {
            timerSpan.classList.remove('blinking');
        }
        if (timeLeft <= 0) {
            clearInterval(timerInterval);
            timeUp();
        }
    }, 1000);
}

function stopTimer() {
    clearInterval(timerInterval);
    if (timerContainer) {
        timerContainer.classList.add('hidden');
        timerSpan.classList.remove('blinking');
    }
}

function timeUp() {
    timeUpPopup.style.display = 'block';
    linkedinButton.disabled = true;
    interpolButton.disabled = true;
}

function handleGameModeChange() {
    gameMode = gameModeSelector.value;
    resetGame();
}

function shareScore() {
    const captureTarget = document.querySelector('.header');
    const settingsToolbar = document.querySelector('.settings-toolbar');
    const shareText = currentTranslations.shareMessage
        .replace('{score}', score)
        .replace('{total}', total);

    // Hide the settings toolbar before taking the screenshot
    if (settingsToolbar) {
        settingsToolbar.style.display = 'none';
    }

    html2canvas(captureTarget, { useCORS: true }).then(canvas => {
        // Show the settings toolbar again after taking the screenshot
        if (settingsToolbar) {
            settingsToolbar.style.display = 'flex';
        }

        canvas.toBlob(blob => {
            if (blob) {
                const file = new File([blob], 'score.png', { type: 'image/png' });
                const shareData = {
                    files: [file],
                    title: currentTranslations.title,
                    text: shareText,
                };

                if (navigator.share && navigator.canShare && navigator.canShare(shareData)) {
                    navigator.share(shareData)
                        .catch((error) => {
                            console.log('Error sharing:', error);
                            navigator.clipboard.writeText(shareText).then(() => {
                                alert('Sharing failed. Score copied to clipboard!');
                            });
                        });
                } else {
                    navigator.clipboard.writeText(shareText).then(() => {
                        alert('Score copied to clipboard!');
                    });
                }
            }
        }, 'image/png');
    }).catch(err => {
        // Ensure the settings toolbar is shown even if there's an error
        if (settingsToolbar) {
            settingsToolbar.style.display = 'flex';
        }
        console.error('Error generating screenshot:', err);
        navigator.clipboard.writeText(shareText).then(() => {
            alert('Could not generate image, score copied to clipboard instead!');
        });
    });
}


// --- Game Logic Functions (Only for index.html) ---

function nextImage() {
    if (!profilePicture) return; // Exit if not on the game page
    stopTimer();
    feedbackEl.textContent = '';
    feedbackEl.className = '';
    descriptionEl.textContent = '';
    nextButton.classList.add('hidden');
    linkedinButton.disabled = false;
    interpolButton.disabled = false;

    if (remainingImages.length === 0) {
        remainingImages = [...images]; // Reshuffle
    }

    const randomIndex = Math.floor(Math.random() * remainingImages.length);
    currentImage = remainingImages.splice(randomIndex, 1)[0];
    profilePicture.src = currentImage.src;
    total++;
    totalSpan.textContent = total;
    startTimer();
}

function checkAnswer(guess) {
    stopTimer();
    if (guess === currentImage.type) {
        score++;
        scoreSpan.textContent = score;
        feedbackEl.textContent = currentTranslations.correct;
        feedbackEl.className = 'correct';
    } else {
        feedbackEl.textContent = currentTranslations.wrong;
        feedbackEl.className = 'incorrect';
    }

    descriptionEl.textContent = currentTranslations[currentImage.descriptionKey];

    if (currentImage.type === 'interpol') {
        const name = currentImage.name;
        const recordUrl = currentImage.recordUrl;
        const policeRecordLink = document.createElement('a');
        policeRecordLink.href = recordUrl;
        policeRecordLink.textContent = currentTranslations.policeRecord;
        policeRecordLink.target = '_blank';
        descriptionEl.innerHTML = `<b>${name}</b> - ${currentTranslations[currentImage.descriptionKey]} <br> `;
        descriptionEl.appendChild(policeRecordLink);
    }

    linkedinButton.disabled = true;
    interpolButton.disabled = true;

    if (autoAdvance) {
        setTimeout(nextImage, 3000);
    } else {
        nextButton.classList.remove('hidden');
    }
}

function resetGame() {
    score = 0;
    total = 0;
    scoreSpan.textContent = score;
    totalSpan.textContent = total;
    remainingImages = [...images];
    if (timeUpPopup) timeUpPopup.style.display = 'none';
    nextImage();
}

// --- Initialization ---

function initializeCommonComponents() {
    const userLang = navigator.language || navigator.userLanguage;
    const langCode = userLang.split('-')[0];
    const initialLang = translations[langCode] ? langCode : 'en';

    if (languageSelector) {
        languageSelector.addEventListener('change', () => loadLanguage(languageSelector.value));
    }
    if (instructionsLanguageSelector) {
        instructionsLanguageSelector.addEventListener('change', () => loadLanguage(instructionsLanguageSelector.value));
    }

    // Load initial language
    loadLanguage(initialLang);

    // Cookie Banner
    const banner = document.getElementById('cookie-banner');
    const acceptButton = document.getElementById('cookie-accept');
    if (banner && acceptButton) {
        if (localStorage.getItem('cookieConsent') === 'true') {
            banner.style.display = 'none';
        } else {
            banner.style.display = 'flex';
        }
        acceptButton.addEventListener('click', () => {
            localStorage.setItem('cookieConsent', 'true');
            banner.style.display = 'none';
        });
    }

    // Request Image Popup (only on main page)
    const requestBtn = document.getElementById('request-image-btn');
    const popup = document.getElementById('request-image-popup');
    const closeBtn = document.querySelector('.close-btn');
    if (requestBtn && popup && closeBtn) {
        requestBtn.addEventListener('click', () => popup.style.display = 'block');
        closeBtn.addEventListener('click', () => popup.style.display = 'none');
        window.addEventListener('click', (event) => {
            if (event.target === popup) popup.style.display = 'none';
        });
    }

    // Instructions Popup (only on main page)
    const instructionsBtn = document.getElementById('instructions-btn');
    const instructionsPopup = document.getElementById('instructions-popup');
    const closeInstructionsBtn = document.querySelector('.close-instructions-btn');
    const playButton = document.getElementById('play-button');
    if (instructionsBtn && instructionsPopup && closeInstructionsBtn && playButton) {
        instructionsPopup.style.display = 'block'; // Show on initial load
        instructionsBtn.addEventListener('click', () => instructionsPopup.style.display = 'block');
        closeInstructionsBtn.addEventListener('click', () => instructionsPopup.style.display = 'none');
        playButton.addEventListener('click', () => instructionsPopup.style.display = 'none');
        window.addEventListener('click', (event) => {
            if (event.target === instructionsPopup) instructionsPopup.style.display = 'none';
        });
    }
}

function initializeGame() {
    if (!profilePicture) return; // Make sure we are on the game page

    linkedinButton.addEventListener('click', () => checkAnswer('linkedin'));
    interpolButton.addEventListener('click', () => checkAnswer('interpol'));
    nextButton.addEventListener('click', nextImage);
    autoAdvanceSwitch.addEventListener('change', () => autoAdvance = autoAdvanceSwitch.checked);
    gameModeSelector.addEventListener('change', handleGameModeChange);
    playAgainButton.addEventListener('click', resetGame);
    shareScoreButton.addEventListener('click', shareScore);

    nextImage(); // Load the first image
}

document.addEventListener('DOMContentLoaded', () => {
    initializeCommonComponents();

    // Only initialize the game if we are on the main game page
    if (document.getElementById('profile-picture')) {
        initializeGame();
    }
});
